public class Helicopter {
    public static void main(String[]args) throws InterruptedException {
        while (true) {
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println("               +========");
            System.out.println("              | |");
            System.out.println("         //-------------");
            System.out.println("=========             ()\\");
            System.out.println("         \\               \\");
            System.out.println("          \\                ]");
            System.out.println("            ----------------");
            System.out.println("               I         I    ");
            System.out.println("            -------------------");
            Thread.sleep(100);
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println("       ========+");
            System.out.println("              | |");
            System.out.println("         //-------------");
            System.out.println("=========             ()\\");
            System.out.println("         \\               \\");
            System.out.println("          \\                ]");
            System.out.println("            ----------------");
            System.out.println("               I         I    ");
            System.out.println("            -------------------");
            Thread.sleep(100);
        }
    }
}
